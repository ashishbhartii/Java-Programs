// WAJP to Input a string and print it.

import java.util.Scanner;
public class String01 {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        System.out.println();
        System.out.print("Enter a String : ");
        String str = s.next();
        System.out.println();
        System.out.println(str);
        System.out.println();
    }
}
